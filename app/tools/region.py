REGIONS: tuple[str, ...] = (
    "서울", "경기도", "강원도", "인천", "전라도", "경상도", "충청도", "제주도",
)


def classify_region(address: str | None) -> str | None:
    """주소 문자열(plmk_addr_basic 또는 road_addr_basic)의 첫 토큰으로 8개 권역 중 하나로 분류.

    분류 불가한 주소(빈 값, 또는 알려지지 않은 첫 토큰)는 None 반환 — 임의로 추정하지 않음.
    """
    if not address:
        return None
    tokens = address.split()
    if not tokens:
        return None
    return _REGION_PREFIXES.get(tokens[0])
